num1 = float(input("Введите первое число: "))
num2 = float(input("Введите второе число: "))

sum = num1 + num2
subtract = num1 - num2
multiply = num1 * num2
divide = num1 / num2
division_complete = num1 // num2
percent = num1 % num2
degree = num1 ** num2

print(f"Сложение: {sum}")
print(f"Вычитание: {subtract}")
print(f"Умножение: {multiply}")
print(f"Деление: {divide}")
print(f"Деление нацело: {division_complete}")
print(f"Остаток от деления: {percent}")
print(f"Возведение в степень: {degree}")
